
/**
 * @module Matemática
 */

/**
 * 
 * @param {number} x Primer número
 * @param {number} y Segundo número
 * @returns {number} Resultado Final
 */
const suma = (x,y) => x+y;

/**
 * 
 * @param {number} x Primer número
 * @param {number} y Segundo número
 * @returns {number} Resultado Final
 */
const resta = (x,y) => x-y;


module.exports = {
    suma, resta
}
